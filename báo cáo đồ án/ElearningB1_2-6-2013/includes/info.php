<b>Giới thiệu đề tài</b><br/>
<b>Tên đề tài: <i>Xây dựng hệ thống E-Learning cho các khóa học tiếng Anh</i></b><br/><br/>
<b>&raquo;| Chuẩn B1 Châu Âu là gì?</b><br/>
Là cấp độ “Independent User” (sử dụng độc lập): người sử dụng có thể hiểu được các ý chính của một diễn ngôn tiêu chuẩn (standard input), rõ ràng về các vấn đề quen thuộc trong công việc, trường học, giải trí, v.v… Có khả năng xử lý hầu hết các tình huống có thể xảy ra khi đi đến nơi sử dụng ngôn ngữ. Có thể viết đơn giản nhưng liên kết về các chủ đề quen thuộc hoặc cá nhân quan tâm. Có thể mô tả được những kinh nghiệm, sự kiện, giấc mơ, hy vọng và hoài bão và có thể trình bày ngắn gọn các lý do, giải thích cho ý kiến và kế hoạch của mình.<br/>
Độ dài của đề thi cho 4 kỹ năng = 135 phút<br/>
Trình độ năng lực ngoại ngữ của người học được đánh giá qua 4 kỹ năng:<br/>
1. Trình độ nói  <br/>
- Học viên có thể tham gia vào các cuộc trao đổi xã giao không chuẩn bị trước về những chủ đề quen thuộc mà bản thân quan tâm và công việc thường nhật một cách khá tự tin.<br/>
- Có thể cung cấp tương đối chi tiết thông tin, ý kiến, có thể miêu tả, báo cáo và kể lại một sự kiện/tình huống.<br/>
- Có thể sử dụng đa dạng các cấu trúc đơn giản và một số cấu trúc phức tạp nhưng đôi khi lược bỏ/giảm bớt một số thành phần như mạo từ, động từ thì quá khứ. Tuy nhiên vẫn mắc nhiều lỗi phát âm và ngữ pháp, đôi khi gây cản trở giao tiếp.<br/>
- Có thể sử dụng tập hợp những từ dùng hàng ngày, ngữ đoạn và thành ngữ quen thuộc.<br/>
- Có thể đạt mức lưu loát trong giao tiếp thông thường, nhưng vẫn còn nhiều ngập ngừng.<br/>
- Có thể trao đổi qua điện thoại những vấn đề quen thuộc, nhưng còn gặp nhiều khó khăn và lúng túng khi không có hình ảnh/phương tiện nghe nhìn hỗ trợ.<br/>
2. Trình độ nghe<br/>
- Học viên có thể nghe lấy ý chính và xác định được các từ ngữ mấu chốt và những chi tiết quan trọng trong ngôn ngữ nói có độ khó trung bình (các đoạn hội thoại trực diện, các băng hình băng tiếng, các chương trình phát thanh) trong các tình huống giao tiếp nghi lễ hoặc thông thường, về các chủ đề hàng ngày liên quan đến bản thân.<br/>
- Có thể hiểu khá nhiều từ ngữ thông thường và một số lượng rất hạn chế các thành ngữ.<br/>
- Có thể hiểu các diễn ngôn trong ngữ cảnh rõ ràng, về các kinh nghiệm và kiến thức chung.<br/>
- Có thể hiểu các tập hợp chỉ dẫn/ hướng dẫn ngắn, hiểu các câu hỏi trực tiếp về kinh nghiệm bản thân hoặc các chủ đề quen thuộc.
- Có thể đôi lúc yêu cầu người nói nhắc lại.<br/>
- Có thể nghe hiểu, đoán trước được các lời nhắn đơn giản trên điện thoại, nhưng vẫn gặp nhiều khó khăn.<br/>
3. Trình độ đọc  <br/>
- Học viên có thể đọc và nắm ý chính, hiểu các từ chủ yếu và chi tiết quan trọng trong một bài văn xuôi đơn giản (ba đến năm đoạn) hoặc bài đọc không theo hình thức văn xuôi trong ngữ cảnh sử dụng ngôn ngữ có yêu cầu khá cao.<br/>
- Có thể tìm hai hoặc ba chi tiết cụ thể trong các bài đọc dạng văn xuôi, bảng, biểu và lịch trình dùng cho mục đích phân tích, so sánh.<br/>
- Có thể hiểu tin nhắn, thư, lịch trình, hành trình được đánh máy hoặc viết tay rõ ràng.<br/>
- Có thể lấy thông tin về các chủ điểm quen thuộc từ các bài đọc có bố cục rõ ràng, gần gũi với kiến thức nền và trải nghiệm bản thân.<br/>
- Có thể thường xuyên dùng từ điển đơn ngữ dạng đơn giản, thiết kế cho người học như ngôn ngữ thứ hai hoặc ngoại ngữ.<br/>
- Ngôn ngữ của bài đọc chủ yếu là cụ thể và sát với thực tế, với một số mục từ trừu tượng, chứa đựng các khái niệm chuyên môn và có thể đòi hỏi kỹ năng suy luận ở mức độ thấp để hiểu (ví dụ người học có thể đoán một vài từ mới bằng cách nhận ra tiền tố và hậu tố).<br/>
4. Trình độ viết
- Học viên có khả năng hoàn thành các nhiệm vụ viết tương đối phức tạp.<br/>
- Có thể truyền đạt hiệu quả các thông tin quen thuộc trong một bố cục chuẩn quen thuộc.<br/>
- Có thể viết các bức thư và bài viết dài một, hai đoạn.<br/>
- Có thể điền mẫu khai xin việc với các nhận xét ngắn về kinh nghiệm, khả năng, ưu điểm; có thể làm báo cáo.<br/>
- Có thể viết lại các thông tin đơn giản, nghe được hoặc nhìn thấy; có thể ghi chép khi nghe các bài trình bày ngắn hoặc từ các tài liệu tham khảo.<br/>
- Có thể ghi chép thông tin từ bảng, biểu dưới dạng một đoạn văn mạch lạc.<br/>
- Có thể ghi lại lời nhắn điện thoại hàng ngày.<br/>
- Thể hiện khả năng kiểm soát viết tốt với các cấu trúc đơn giản song vẫn gặp khó khăn với một số cấu trúc phức tạp; một số câu/ cụm từ viết ra nghe chưa tự nhiên (ghép từ).<br/>

